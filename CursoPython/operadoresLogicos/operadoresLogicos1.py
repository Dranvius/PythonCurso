print("Operadores Logicos")

#Permiten operar operaciones Booleanas
#and or y not
#operadores binarios y no binarios
verdad = True
falso = False

resultado = verdad and falso   #Las dos condiciones deben ser verdades para que sea verdadero
print(resultado)
resultado = verdad or falso #Solo necesita cunplir una función para todo sea verdadero (Toda la expresión debe ser falsa para que de falso)
print(resultado)
resultado = not verdad #Utilizado para que un booleano cambie su estado a su contrario
print(resultado)