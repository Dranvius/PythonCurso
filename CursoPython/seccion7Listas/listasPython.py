print("Una lista es una estructura de datos que permite almacenar datos de distintos tipos")

listaNueva = ["Alexis","Sergio","Napy","Nayomi"]

print(listaNueva)

#Las listas tienen indices que permiten a acceder a un solo almacenamiento:

#Ejemplo

#Se utilizan los indices del num >= 0. Eso depende de lo q  ue exista almacenado en el la lista

print(listaNueva[0]) #Corresponde a Alexis
print(listaNueva[1]) #Corresponde a Sergio
print(listaNueva[2]) #Corresponde a Napy
print(listaNueva[3]) #Corresponde a Nayomi

print("\nExisten los indices inversos que comienzan desde -1 -2 -3 -4\n")

print(listaNueva[-1])
print(listaNueva[-2])  #De arriva hacia abajo
print(listaNueva[-3])
print(listaNueva[-4])




